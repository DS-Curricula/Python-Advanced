# Define a class named MyClass.
class MyClass:
    # Define a constructor (__init__ method) for the class
    def __init__(self):
        # Initialize an instance variable named public variable and assign it a value.
        self.public_variable = "This is a public variable"


# Create an instance of the MyClass class.
my_class = MyClass()
# Access and print the value of the public_variable instance variable
print(my_class.public_variable)
