string_length = len("Hello World!")  # Length of a string
list_length = len([1, 2, 3, 4, 5])  # Length of a list
tuple_length = len((10, 20, 30))  # Length of a tuple

print(string_length)  # Output: 12
print(list_length)  # Output: 5
print(tuple_length)  # Output: 3

sum_of_list = sum([1, 2, 3, 4, 5])  # Sum of elements in a list
sum_of_tuple = sum((10, 5, 20))  # Sum of elements in a tuple

print(sum_of_list)  # Output: 15
print(sum_of_tuple)  # Output: 35

max_value = max(5, 10, 3)  # Maximum among integers
max_float = max(3.14, 2.71, 5.0)  # Maximum among floats

print(max_value)  # Output: 10
print(max_float)  # Output: 5.0
